n=input()
x1=0
x2=0
x3=0
x4=0
x5=0
if len(n)>=8:
    x4=1
for i in n:
    if i.isdigit():
        x1=1
    if  65<=ord(i)<=90:
        x2=1
    if  97<=ord(i)<=122:
        x3=1 
    if i in ["~","!","@","#","$","%","^","&","*","(",")","_","=","-","/",",",".","?","<",">",";",":","[","]","{","}","|","\\"]:
        x5=1
print(x1+x2+x3+x4+x5)        

