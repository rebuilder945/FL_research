a=eval(input())
s=[]
for i in range(1,0):
    for j in range(10):
        for k in range(10):
            if str(i**3)+str(j**3)+str(k**3)==str(i)+str(j)+str(k):
                m=eval(str(i)+str(j)+str(k))
                if m<a:
                    s.append(m)
                    print(m)
                elif s==[]:
                    print("none")
