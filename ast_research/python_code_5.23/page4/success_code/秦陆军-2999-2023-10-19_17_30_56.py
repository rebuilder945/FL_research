#【问题描述】

#输入一个由字符串构成的列表和两个整数n和m（n和m在输入列表的下标范围以内），交换其中两个元素的值，打印输出交换后的列表。
#【输入形式】

#第一行输入由空格分隔的多个字符串

#第二行输入两个整数n和m，表示元素的位置，两个数字之间用空格区分。
#【输出形式】

#直接使用print函数输出列表
lst = input("请输入由空格分隔的多个字符串: ").split()

# 获取整数输入，n 和 m 表示要交换的位置
n, m = map(int, input("请输入两个整数 n 和 m,表示要交换的位置,用空格分隔: ").split())

# 检查 n 和 m 是否在列表的范围内，如果不在范围内，就不交换
if 0 <= n < len(lst) and 0 <= m < len(lst):
    # 交换位置
    lst[n], lst[m] = lst[m], lst[n]

# 输出交换后的列表
print(lst)

