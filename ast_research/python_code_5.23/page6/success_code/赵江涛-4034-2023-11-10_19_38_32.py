s1 = ['red','yellow','blue']
or1 = input()
or2 = input()
if or1 not in s1 or or2 not in s1 or or1 == or2:
    print('error')
else:
    if or1 != 'red' and or1 != 'red':
        print('green')
    elif or1 != 'blue' and or1 != 'blue':
        print('orange')
    else:
        print('purple')

