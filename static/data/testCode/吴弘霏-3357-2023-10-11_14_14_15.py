print("a:{:.2f}".format(a))
print("v:{:.2f}".format(v))
print("The acceleration of {} is {1:%,2f} M / s, the take-off speed is {:.2f} M / s, and the shortest take-off runway length is {:.2f} M.")(sName,a,v,length)
